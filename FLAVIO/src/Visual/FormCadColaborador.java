    public void listarColaborador(){
        
        String sql = "Select *from colaborador";
        
        try{
            pst = conecta.prepareStatement(sql);
            rs = pst.executeQuery();
            tblColaborador.setModel(DbUtils.resultSetToTableModel(rs));
            
        }
        
        catch(SQLException error){
            JOptionPane.showMessageDialog(null, error);
        }
    }
    
    //metodo para cadastrar um colaborador no banco POSTGRES na ordem que encontra-se abaixo
    public void cadastrarColaborador(){
        
        String sql = "INSERT INTO colaborador values (?,?,?,?)";
        
        try{
            
            pst = conecta.prepareStatement(sql);
            pst.setString(1, txtMatricula.getText());
            pst.setString(2, txtNome.getText());
            pst.setString(3, txtLogin.getText());
            pst.setString(4, txtSenha.getText());          
            pst.execute();
            JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!");
            listarColaborador();
        }
        
        catch(SQLException error){
            JOptionPane.showMessageDialog(null, error); 
        }
    }
    
    public void pesquisarColaborador(){
        
        String sql = "Select *from colaborador where nome like ?";
        
        try{
            pst = conecta.prepareStatement(sql);
            pst.setString(1, txtPesquisar.getText()+"%");
            rs = pst.executeQuery();
            tblColaborador.setModel(DbUtils.resultSetToTableModel(rs));
        }
        
        catch(SQLException error){
            JOptionPane.showMessageDialog(null, error);
        }
    }
    
    public void mostrarItens(){
        
        int seleciona = tblColaborador.getSelectedRow();
        txtMatricula.setText(tblColaborador.getModel().getValueAt(seleciona,9).toString());
        txtNome.setText(tblColaborador.getModel().getValueAt(seleciona,0).toString());
        txtLogin.setText(tblColaborador.getModel().getValueAt(seleciona,1).toString());
        txtSenha.setText(tblColaborador.getModel().getValueAt(seleciona,2).toString());
    }
    
    //metodo para editar um colaborador no banco POSTGRES na ordem que encontra-se abaixo
    public void editarColaborador(){
            
            String sql = "Update colaborador set nome = ?, login = ?, senha = ?, where matricula = ? ";
            
            try{
                
                pst = conecta.prepareStatement(sql);
                pst.setString(1,txtMatricula.getText());
                pst.setString(2, txtNome.getText());
                pst.setString(3, txtLogin.getText());
                pst.setString(4, txtSenha.getText()); 
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null,"Cadastro Atualizado com sucesso!");
                listarColaborador();
            }
            
            catch(SQLException error){
                
                JOptionPane.showMessageDialog(null, error);
                
            }
    }
    public void limparCampos(){
        
        txtMatricula.setText("");
        txtNome.setText("");
        txtLogin.setText("");
        txtSenha.setText("");
        
    }
            
            
